package de.doccrazy.shared.game.svg;

public class Arc {
    public final float x, y;
    public final float r;
    public final float a1, a2;

    public Arc(float x, float y, float r, float a1, float a2) {
        this.x = x;
        this.y = y;
        this.r = r;
        this.a1 = a1;
        this.a2 = a2;
    }
}
